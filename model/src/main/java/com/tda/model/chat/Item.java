package com.tda.model.chat;

public class Item {
	private String f;
	private String s;
	private String m;

	public Item() {
		
	}

	public String getF() {
		return f;
	}
	public void setF(String f) {
		this.f = f;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public String getM() {
		return m;
	}
	public void setM(String m) {
		this.m = m;
	}

	
}
